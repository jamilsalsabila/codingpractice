enc = {
	' ': '0b11', 'e': '0b101',
	't': '0b1001', 'o': '0b10001',
	'n': '0b10000', 'a': '0b011',
	's': '0b0101', 'i': '0b01001',
	'r': '0b01000', 'h': '0b0011',
	'd': '0b00101', 'l': '0b001001',
	'!': '0b001000', 'u': '0b00011',
	'c': '0b000101', 'f': '0b000100',
	'm': '0b000011', 'p': '0b0000101',
	'g': '0b0000100', 'w': '0b0000011',
	'b': '0b0000010', 'y': '0b0000001',
	'v': '0b00000001', 'j': '0b000000001',
	'k': '0b0000000001', 'x': '0b00000000001',
	'q': '0b000000000001', 'z': '0b000000000000'
}

s = input().strip()
bits = ''
for c in s:
	bits += enc[c][2:]

if len(bits)%8>0:
	bits += '0'*(8-(len(bits)%8))

for i in range(0, len(bits), 8):
	sbit = bits[i:i+8]
	shex = hex(int(sbit, 2))[2:]
	if len(shex) == 1:
		shex = '0' + shex
	print(shex.upper(), end=' ')
print()
